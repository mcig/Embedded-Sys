.text
.global main
; EEPROM Read/Write Regs
.EQU EECR, 0x1F
.EQU EEDR, 0x20
.EQU EEARL,  0x21
.EQU EEARH,  0x22
.EQU EERE, 0
.EQU EEWE, 1
.EQU EEMWE, 2
main: 
LDI R21,0x01
LDI R20,0x2A

POLL:
SBIC EECR, EEWE
RJMP POLL

OUT EEARL, R20
OUT EEARH, R21

LDI R22, 0
OUT EEDR, R22

SBI EECR, EEMWE
SBI EECR, EEWE
